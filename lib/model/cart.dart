class CartData {
  final String title;
  final int quantity;
  final String image;
  final double size;
  final int price;
  final String productID;
  final String userID;
  const CartData({
    required this.title,
    required this.quantity,
    required this.image,
    required this.size,
    required this.price,
    required this.userID,
    required this.productID,
  });
}
