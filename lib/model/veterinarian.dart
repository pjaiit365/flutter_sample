class VetData {
  final String name;
  final int number;
  final String region;
  final String location;
  final int starsRating;
  final String experience;
  final String vetID;
  final String pic;
  final String experienceNumber;
  const VetData({
    required this.vetID,
    required this.number,
    required this.name,
    required this.experience,
    required this.starsRating,
    required this.location,
    required this.region,
    required this.pic,
    required this.experienceNumber,
  });
}
